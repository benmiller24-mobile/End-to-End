export const SHILOH_CATALOG = (() => {
  const m = new Map();
  for (const l of SHILOH_RAW_SKU_DATA.split("\n")) {
    if (!l) continue;
    const [s, p, r, t] = l.split("|");
    if (s && p) m.set(s, { s, p: parseFloat(p), r, t });
  }
  return m;
})();

export function findShilohSku(sku) {
  const hit = SHILOH_CATALOG.get(sku);
  if (hit) return hit;
  // fall back to the Eclipse catalog (flagged interim) so pricing always resolves
  return findEclipseSku(sku);
}

